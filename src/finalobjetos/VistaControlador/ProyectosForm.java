    public void clearJTable(){
        DefaultTableModel model = (DefaultTableModel) this.tabla.getModel();
        int filas = model.getRowCount();
        for (int i = 0; i < filas; i++) {
            model.removeRow(0);
        }
    }
    
    public void addRowToJTable(ArrayList <Proyecto> lista){
        DefaultTableModel model = (DefaultTableModel) this.tabla.getModel();
        Object rowData[] = new Object[1];
        if (!lista.isEmpty()) {
            for (int i = 0; i < (lista.size()); i++) {
                rowData[0] = lista.get(i).getNombre();
                model.addRow(rowData);
            }
        }
        
    }
    
    void inicializar() {
        this.clearJTable();
        this.addRowToJTable(this.usuario.getProyectos());
        this.etiquetaError.setVisible(false);
    }

