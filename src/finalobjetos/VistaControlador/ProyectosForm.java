    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.tabla.getSelectedRow() != -1) {
            this.setVisible(false);
            Proyecto proyecto = this.usuario.getProyectos().get(this.tabla.getSelectedRow());
            this.items.setProyecto(proyecto);
            this.items.setEquipos(this.equipos);
            this.items.setUsuario(this.usuario);
            this.items.inicializarItems(proyecto.getItems());
            this.items.setVisible(true);
        }else{
            this.etiquetaError.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

