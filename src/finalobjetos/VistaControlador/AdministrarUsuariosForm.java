    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (this.tabla.getSelectedRow() != -1) {
            ArrayList <Proyecto> aux = this.usuarios.get(this.tabla.getSelectedRow()).getProyectos();
            for (int i = 0; i < aux.size(); i++) {
                aux.get(i).getMiembros().remove(this.usuarios.get(this.tabla.getSelectedRow()));
            }
            int equipo = this.equipos.indexOf(this.usuarios.get(this.tabla.getSelectedRow()).getEquipo());
            this.equipos.get(equipo).getMiembros().remove(this.usuarios.get(this.tabla.getSelectedRow()));
            this.usuarios.remove(this.tabla.getSelectedRow());
            this.llenarTabla();
        }else{
            this.etiquetaError.setVisible(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


