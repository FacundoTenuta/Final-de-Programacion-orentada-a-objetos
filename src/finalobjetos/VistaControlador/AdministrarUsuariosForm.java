    public void addRowToJTable(ArrayList <Usuario> lista){
        DefaultTableModel model = (DefaultTableModel) this.tabla.getModel();
        Object rowData[] = new Object[5];
        if (!lista.isEmpty()) {
            for (int i = 0; i < (lista.size()); i++) {
                rowData[0] = lista.get(i).getUsuarioID();
                rowData[1] = lista.get(i).getNombre();
                rowData[2] = lista.get(i).getContraseña();
                rowData[3] = lista.get(i).getAdmin();
                rowData[4] = lista.get(i).getEquipo();
                model.addRow(rowData);
            }
        }
        
    }
    
    public void clearJTable(){
        DefaultTableModel model = (DefaultTableModel) this.tabla.getModel();
        int filas = model.getRowCount();
        for (int i = 0; i < filas; i++) {
            model.removeRow(0);
        }
    }

    public void llenarTabla() {
        this.clearJTable();
        this.addRowToJTable(this.usuarios);
    }
