    void setItem(Item itemVisto) {
        this.item = itemVisto;
        this.etiquetaNombre.setText(this.item.getNombre());
        this.etiquetaEstado.setText(this.item.getEstadoActual().getNombre());
        this.etiquetaPrioridad.setText(this.item.getPrioridad());
        this.etiquetaResponsable.setText(this.item.getEstadoActual().getResponsable());
        this.etiquetaEquipo.setText(this.item.getEquipo().getNombre());
        this.etiquetaTipo.setText(this.item.getTipoItem().getNombre());
        this.llenarComboEstados();
        this.llenarComboResponsables();
        
    }

    private void llenarComboEstados() {
        this.estadosCombo.removeAllItems();
        ArrayList <String> lista = this.item.getTipoItem().getWorkflow().getProximosEstados(this.item.getTipoItem().getNombre(), this.item.getEstadoActual());
        for (int i = 0; i < (lista.size()); i++) {
                this.estadosCombo.addItem(lista.get(i));
        }
    }

    void setProyecto(Proyecto proyecto) {
        this.proyecto = proyecto;
    }

    private void llenarComboResponsables() {
        this.comboResponsables.removeAllItems();
        ArrayList <Usuario> lista = this.proyecto.getMiembros();
        for (int i = 0; i < lista.size(); i++) {
            this.comboResponsables.addItem(lista.get(i).getNombre());
        }
    }

    private void verificarResponsable() {
        if ((this.item.getEstadoActual().getResponsable() == this.usuario.getNombre()) || this.usuario.getAdmin()) {
            this.etiquetaNuevoResponsable.setVisible(true);
            this.etiquetaProximoEstado.setVisible(true);
            this.comboResponsables.setVisible(true);
            this.estadosCombo.setVisible(true);
            this.botonAsignar.setVisible(true);
            this.botonAvanzarEstado.setVisible(true);
        }else{
            this.etiquetaNuevoResponsable.setVisible(false);
            this.etiquetaProximoEstado.setVisible(false);
            this.comboResponsables.setVisible(false);
            this.estadosCombo.setVisible(false);
            this.botonAsignar.setVisible(false);
            this.botonAvanzarEstado.setVisible(false);
        }
    }

    void setUsuario(Usuario usuario) {
        this.usuario = usuario;
        this.verificarResponsable();
    }
