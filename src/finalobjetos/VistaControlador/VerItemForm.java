    private void botonAvanzarEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAvanzarEstadoActionPerformed
        Object proximo = this.estadosCombo.getItemAt(this.estadosCombo.getSelectedIndex());
        this.item.getEstadoActual().setNombre(proximo.toString());
        Estado historial = new Estado();
        historial.setNombre(proximo.toString());
        historial.setResponsable(this.item.getEstadoActual().getResponsable());
        historial.setFecha(new Date());
        this.item.getHistorial().add(historial);
        this.etiquetaEstado.setText(proximo.toString());
        this.llenarComboEstados();
        Mensaje mensaje = new Mensaje();
        mensaje.setAsunto("Modificacion del estado de un item");
        mensaje.setContenido("Se modifico el estado del item '" + this.item.getNombre() + "' del proyecto '" + this.proyecto.getNombre() + "' al estado de: " + proximo.toString());
        for (int i = 0; i < this.proyecto.getMiembros().size(); i++) {
            this.proyecto.getMiembros().get(i).getMensajes().add(mensaje);
        }
        this.proyecto.getMiembros();
    }//GEN-LAST:event_botonAvanzarEstadoActionPerformed

    private void botonAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAsignarActionPerformed
        Object nuevo = this.comboResponsables.getItemAt(this.comboResponsables.getSelectedIndex());
        this.item.getEstadoActual().setResponsable(nuevo.toString());
        Estado historial = new Estado();
        historial.setNombre(this.item.getEstadoActual().getNombre());
        historial.setResponsable(nuevo.toString());
        historial.setFecha(new Date());
        this.item.getHistorial().add(historial);
        this.etiquetaResponsable.setText(nuevo.toString());
    }//GEN-LAST:event_botonAsignarActionPerformed


