    private String getPrioridad() {
        switch (this.comboPrioridad.getSelectedIndex()){
            case 0: return "Alta";
                
            case 1: return "Media";
                
            case 2: return "Baja";
                
        }
        return null;
    }


    private String getTipoItem() {
        switch (this.comboTipo.getSelectedIndex()){
            case 0: return "reporte de bug";
            case 1: return "ampliacion";
            case 2: return "mejora";
            case 3: return "nuevo requerimiento";
        }
            
        return null;
    }

    void setUsuario(Usuario usuario) {
        this.usuario = usuario;
    }

    void setEquipos(ArrayList<Equipo> equipos) {
        this.equipos = equipos;
        for (int i = 0; i < this.equipos.size(); i++) {
            this.comboEquipo.addItem(this.equipos.get(i));
        }
    }

    private void limpiarEquipos() {
        this.comboEquipo.removeAllItems();
    }

    private Equipo getEquipo() {
        return this.equipos.get(this.comboEquipo.getSelectedIndex());
    }
