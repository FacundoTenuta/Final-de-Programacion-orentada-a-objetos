    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        this.setVisible(false);
        if (this.verProyecto == null) {
            this.limpiarEquipos();
            this.proyectoNuevo.setVisible(true);
        }else{
            this.verProyecto.inicializarItems(items);
            this.limpiarEquipos();
            this.verProyecto.setVisible(true);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (this.nombreText != null) {
            Item nuevo = new Item();
            nuevo.setNombre(this.nombreText.getText());
            nuevo.setPrioridad(this.getPrioridad());
            nuevo.setTipoItem(this.getTipoItem());
            nuevo.setEquipo(this.getEquipo());
            Estado historial = new Estado();
            historial.setResponsable(this.usuario.getNombre());
            nuevo.getHistorial().add(historial);
            Estado estado = new Estado();
            estado.setResponsable(this.usuario.getNombre());
            nuevo.setEstadoActual(estado);
            this.items.add(nuevo);
            this.setVisible(false);
            if (this.verProyecto == null) {
                this.proyectoNuevo.setVisible(true);
            }else{
                this.verProyecto.inicializarItems(items);
                this.verProyecto.setVisible(true);
            }
            this.etiquetaError.setVisible(false);
        }else{
            this.etiquetaError.setVisible(true);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed


